    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String itemName=jTextField1.getText();
        String itemPrice=jTextField2.getText();
        String itemCategory= (String)jComboBox1.getSelectedItem();
        String itemDescription=jTextArea1.getText();
        String itemQuantity=jTextField5.getText();
        
        FileInputStream fis=null;
        try 
        {
          fis= new FileInputStream(file);  
        } 
        catch (Exception e) {
            e.printStackTrace();
        }
        
        try(
               Connection con = dbconnection.DbConnection.getConnect();
                PreparedStatement ps=con.prepareStatement("update add_item set item_name=?,item_price=?,item_category=?, "
                        + "item_discreption=?,item_quantity=?,item_image=? where item_id=?");
                
                )
        {
            ps.setString(1, itemName);
            ps.setString(2, itemPrice);
            ps.setString(3, itemCategory);
            ps.setString(4, itemDescription);
            ps.setString(5, itemQuantity);
            ps.setBinaryStream(6, fis, file.length());
            ps.setString(7, itemId);
            int i= ps.executeUpdate();
            if (i>0) {
                
                JOptionPane.showMessageDialog(rootPane, "Item Udated Successfully!!!!");
            }
            else{
            JOptionPane.showMessageDialog(rootPane, "Oops! Failed update","Error",JOptionPane.ERROR_MESSAGE);
            
            }
        } 
        catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

