    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        
        int row=jTable2.getSelectedRow();
         itemId=(String)jTable2.getValueAt(row, 1);
        String itemName,itemPrice,itemCategory,itemQuantity,itemDesc;
        
        try (
                
              Connection con= dbconnection.DbConnection.getConnect();
                PreparedStatement ps=con.prepareStatement("select * from add_item where item_id='"+itemId+"'");
                ResultSet rs=ps.executeQuery();
                
                )
        {
            while (rs.next()) {                
                
                itemName=rs.getString("item_name");
                itemPrice=rs.getString("item_price");
                itemCategory=rs.getString("item_category");
                itemDesc=rs.getString("item_discreption");
                itemQuantity=rs.getString("item_quantity");
                BufferedImage im= ImageIO.read(rs.getBinaryStream("item_image"));
                jLabel2.setIcon(new ImageIcon(im.getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(), Image.SCALE_DEFAULT)));
                
                jTextField1.setText(itemName);
                jTextField2.setText(itemPrice);
                jComboBox1.setSelectedItem(itemCategory);
                jTextField5.setText(itemQuantity);
                jTextArea1.setText(itemDesc);
                
            }
        } 
        catch (SQLException |IOException e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jTable2MouseClicked

