    void generateId(){
    
     int total=1;
        
        try (
                
              Connection con= dbconnection.DbConnection.getConnect();
                PreparedStatement ps=con.prepareStatement("select count(*) As total from add_item");
                ResultSet rs=ps.executeQuery();
                
                )
        {
            if(rs.next()) 
            {                
               total=rs.getInt(total);
               
               jTextField1.setText("ItemId-"+(total+1));
               jTextField1.setEditable(false);
               
            }
        } 
        catch (SQLException e) {
            e.printStackTrace();
        }
    }
