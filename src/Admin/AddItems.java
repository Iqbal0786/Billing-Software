    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         
        FileInputStream fis=null;
        if (file==null) {
            
            JOptionPane.showMessageDialog(rootPane, "Select Image First!!!");
        }
        else{
        try {
            
            fis= new FileInputStream(file);
        } 
        catch (Exception e) {
            e.printStackTrace();
        }
         
         String item_id=jTextField1.getText();
         String item_name=jTextField2.getText();
         String item_price=jTextField3.getText();
         String item_category=(String)jComboBox1.getSelectedItem();
         String item_discreption=jTextArea1.getText();
         String quantity= jTextField4.getText();
         try (
                 Connection con=dbconnection.DbConnection.getConnect();
                 PreparedStatement ps=con.prepareStatement("insert into add_item values(?,?,?,?,?,?,?)");
                 
                 )
         
         {
            ps.setString(1, item_id);
            ps.setString(2, item_name);
            ps.setString(3, item_price);
            ps.setString(4, item_category);
            ps.setString(5, item_discreption);
            ps.setString(6, quantity);
            ps.setBinaryStream(7, fis, file.length());
            int i=ps.executeUpdate();
             if (i>0) {
                 JOptionPane.showMessageDialog(rootPane, "Item has Added Successfully!!!!!");
                 jTextField1.setText("");
                 jTextField2.setText("");
                 jTextField3.setText("");
                 jComboBox1.setSelectedIndex(0);
                 jTextField4.setText("");
                 jTextArea1.setText("");
                  jLabel9.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/notfound.png")));
                  
                 generateId();
             }
             else{
              JOptionPane.showMessageDialog(rootPane, "There is an error!! Please Try Again","Error",JOptionPane.ERROR_MESSAGE);
             }
        } 
         catch (Exception e) {
             e.printStackTrace();
        }
        
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

